    public void loadJobDetails() {
    try {
        BufferedReader reader = new BufferedReader(new FileReader("appointments.txt"));
        String line;

        // Loop through the file line by line
        while ((line = reader.readLine()) != null) {
            String[] jobData = line.split(",");
            
            // Check if the ID in the file matches the ID passed from the dashboard
            if (jobData[0].trim().equals(currentJobId)) {
                
                // We found the match! Now set the text fields.
                // (Make sure the index numbers match how your text file is organized)
                jTextField1.setText(jobData[0].trim()); // Appointment ID
                jTextField2.setText(jobData[1].trim()); // Customer Name
                jTextField3.setText(jobData[2].trim()); // Car Plate
                jTextField4.setText(jobData[3].trim()); // Service Type
                
                break; // Stop searching since we found what we needed
            }
        }
        reader.close();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading job details: " + e.getMessage());
    }
    }
